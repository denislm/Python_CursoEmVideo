def somar(a = 0, b = 0, c = 0):
    '''
    -> Faz a soma de 3 valores e imprime o resultado na tela, sendo:
    :param a: O primeiro valor
    :param b: O segundo valor
    :param c: O terceiro valor
    :return: sem retorno
    '''
    s = a + b + c
    print(f'A soma de {a} + {b} + {c} = {s}')

somar(1, 2, 3)
somar(23, 35)
somar(b= 54, c= 32)
somar()